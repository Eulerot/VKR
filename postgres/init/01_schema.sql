-- ============================================================================
-- Схема БД для PostgreSQL
-- ООО «МехЗемСтрой»
-- ============================================================================
BEGIN;

DROP TABLE IF EXISTS repair_assignments CASCADE;
DROP TABLE IF EXISTS monthly_repair_plan CASCADE;
DROP TABLE IF EXISTS brigade_availability CASCADE;
DROP TABLE IF EXISTS brigades CASCADE;
DROP TABLE IF EXISTS material_demand CASCADE;
DROP TABLE IF EXISTS material_norms CASCADE;
DROP TABLE IF EXISTS repair_plan CASCADE;
DROP TABLE IF EXISTS monthly_resources CASCADE;
DROP TABLE IF EXISTS repair_tech_cards CASCADE;
DROP TABLE IF EXISTS repair_requests CASCADE;
DROP TABLE IF EXISTS repair_acts CASCADE;
DROP TABLE IF EXISTS machine_events CASCADE;
DROP TABLE IF EXISTS materials CASCADE;
DROP TABLE IF EXISTS units CASCADE;
DROP TABLE IF EXISTS machines CASCADE;

-- ============================================================================
-- 1. Справочник единиц измерения
-- ============================================================================
CREATE TABLE units (
    unit_id      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    unit_symbol  VARCHAR(20)  NOT NULL UNIQUE,
    unit_name    VARCHAR(100) NOT NULL
);

INSERT INTO units (unit_symbol, unit_name) VALUES
