('кг', 'килограмм'),
('л',  'литр'),
('м',  'метр'),
('компл', 'комплект'),
('ч',  'час');

-- ============================================================================
-- 2. Справочник машин и механизмов
-- ============================================================================
CREATE TABLE machines (
    machine_id       VARCHAR(20) PRIMARY KEY,
    model            VARCHAR(100) NOT NULL,
    plate_number     VARCHAR(20),
    serial_number    VARCHAR(50),
    commission_year  SMALLINT CHECK (commission_year IS NULL OR commission_year BETWEEN 1900 AND 2100),
    notes            TEXT
);

-- ============================================================================
-- 3. Путевые листы / события эксплуатации
-- ============================================================================
CREATE TABLE machine_events (
    event_id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_date        DATE NOT NULL,
    machine_id        VARCHAR(20) NOT NULL,
    driver_name       VARCHAR(100),
    work_object       VARCHAR(100),
    start_hours       INTEGER CHECK (start_hours IS NULL OR start_hours >= 0),
    end_hours         INTEGER CHECK (end_hours IS NULL OR end_hours >= 0),
    operation_status   VARCHAR(50),
    location          VARCHAR(100),
    technical_notes   VARCHAR(20) NOT NULL CHECK (technical_notes IN ('исправна', 'неисправна')),
    CONSTRAINT fk_machine_events_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE INDEX idx_machine_events_machine_date
    ON machine_events (machine_id, event_date DESC, event_id DESC);

-- ============================================================================
-- 4. Акты приемки-сдачи машины после ремонта
-- ============================================================================
CREATE TABLE repair_acts (
    repair_act_id   INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    machine_id      VARCHAR(20) NOT NULL,
    repair_type     VARCHAR(50) NOT NULL,
    start_date      DATE,
    end_date        DATE NOT NULL,
    hours_before    INTEGER CHECK (hours_before IS NULL OR hours_before >= 0),
    hours_after     INTEGER CHECK (hours_after IS NULL OR hours_after >= 0),
    conclusion      VARCHAR(20) NOT NULL CHECK (conclusion IN ('исправна', 'неисправна')),
    CONSTRAINT fk_repair_acts_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE INDEX idx_repair_acts_machine_enddate
    ON repair_acts (machine_id, end_date DESC, repair_act_id DESC);

-- ============================================================================
-- 5. Реестр заявок на ремонт
-- ============================================================================
CREATE TABLE repair_requests (
    request_id               VARCHAR(20) PRIMARY KEY,
    request_status           VARCHAR(20) NOT NULL CHECK (request_status IN ('новая', 'в работе', 'согласована', 'отклонена', 'выполнена')),
    machine_id               VARCHAR(20) NOT NULL,
    model                    VARCHAR(100) NOT NULL,
    priority_weight          INTEGER NOT NULL CHECK (priority_weight >= 0),
    motohours_at_request     INTEGER CHECK (motohours_at_request IS NULL OR motohours_at_request >= 0),
    forecast_cost            NUMERIC(12,2),
    repair_type              VARCHAR(50) NOT NULL,
    critical_parts_required  VARCHAR(3) NOT NULL CHECK (critical_parts_required IN ('да', 'нет')),
    required_qualification   SMALLINT NOT NULL CHECK (required_qualification BETWEEN 1 AND 10),
    desired_month            SMALLINT CHECK (desired_month IS NULL OR desired_month BETWEEN 1 AND 12),
    notes                    TEXT,
    CONSTRAINT fk_repair_requests_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE INDEX idx_repair_requests_status_priority
    ON repair_requests (request_status, priority_weight DESC, desired_month);

-- ============================================================================
-- 6. Технологические карты ремонтных операций
-- ============================================================================
CREATE TABLE repair_tech_cards (
    techcard_id               INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    repair_type               VARCHAR(50) NOT NULL,
    machine_id                VARCHAR(20) NOT NULL,
    labor_hours               INTEGER NOT NULL CHECK (labor_hours >= 0),
    required_qualification    SMALLINT NOT NULL CHECK (required_qualification BETWEEN 1 AND 10),
    operations_description    TEXT,
    notes                     TEXT,
    CONSTRAINT fk_repair_tech_cards_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT unique_repair_machine UNIQUE (repair_type, machine_id)
);

-- ============================================================================
-- 7. Ресурсы и наличие запчастей по месяцам
-- ============================================================================
CREATE TABLE monthly_resources (
    resource_id             INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    month_no                SMALLINT NOT NULL UNIQUE CHECK (month_no BETWEEN 1 AND 12),
    available_hours         INTEGER CHECK (available_hours IS NULL OR available_hours >= 0),
    budget                  NUMERIC(12,2),
    max_units_in_repair     INTEGER CHECK (max_units_in_repair IS NULL OR max_units_in_repair >= 0),
    critical_parts_available VARCHAR(3) NOT NULL CHECK (critical_parts_available IN ('да', 'нет')),
    notes                   TEXT
);

-- ============================================================================
-- 8. Годовой план ремонтов
-- ============================================================================
CREATE TABLE repair_plan (
    plan_id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_id              VARCHAR(20) NOT NULL UNIQUE,
    machine_id              VARCHAR(20) NOT NULL,
    model                   VARCHAR(100) NOT NULL,
    repair_type             VARCHAR(50) NOT NULL,
    required_qualification  SMALLINT NOT NULL CHECK (required_qualification BETWEEN 1 AND 10),
    labor_hours             INTEGER NOT NULL CHECK (labor_hours >= 0),
    priority_weight         INTEGER NOT NULL CHECK (priority_weight >= 0),
    forecast_cost           NUMERIC(12,2),
    assigned_month          SMALLINT CHECK (assigned_month IS NULL OR assigned_month BETWEEN 1 AND 12),
    CONSTRAINT fk_repair_plan_request
        FOREIGN KEY (request_id) REFERENCES repair_requests(request_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT fk_repair_plan_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE INDEX idx_repair_plan_machine_month
    ON repair_plan (machine_id, assigned_month);

-- ============================================================================
-- 9. Справочник материалов и запасных частей
-- ============================================================================
CREATE TABLE materials (
