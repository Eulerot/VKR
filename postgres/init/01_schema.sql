-- ============================================================================
-- 10. Нормативные карты ремонта
-- ============================================================================
CREATE TABLE material_norms (
    norm_id                 INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    repair_type             VARCHAR(50) NOT NULL,
    model                   VARCHAR(100) NOT NULL,
    material_code           VARCHAR(20) NOT NULL,
    consumption_per_repair  NUMERIC(10,3) NOT NULL CHECK (consumption_per_repair >= 0),
    CONSTRAINT fk_material_norms_material
        FOREIGN KEY (material_code) REFERENCES materials(material_code)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT unique_norm UNIQUE (repair_type, model, material_code)
);

CREATE INDEX idx_material_norms_repair_model
    ON material_norms (repair_type, model);

-- ============================================================================
-- 11. Ведомость потребности материалов
-- ============================================================================
CREATE TABLE material_demand (
    demand_id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    target_month      SMALLINT NOT NULL CHECK (target_month BETWEEN 1 AND 12),
    material_code     VARCHAR(20) NOT NULL,
    demand_quantity   NUMERIC(10,3) NOT NULL CHECK (demand_quantity >= 0),
    notes             TEXT,
    CONSTRAINT fk_material_demand_material
        FOREIGN KEY (material_code) REFERENCES materials(material_code)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT unique_demand UNIQUE (target_month, material_code)
);

-- ============================================================================
-- 12. Справочник бригад
-- ============================================================================
CREATE TABLE brigades (
    brigade_number   VARCHAR(20) PRIMARY KEY,
    team_composition TEXT NOT NULL,
    specialization   VARCHAR(20) NOT NULL CHECK (specialization IN ('слесарь', 'электрик', 'сварщик', 'универсальная')),
    qualification    SMALLINT NOT NULL CHECK (qualification BETWEEN 1 AND 10),
    contact          VARCHAR(50),
    notes            TEXT
);

-- ============================================================================
-- 13. График занятости бригад
-- ============================================================================
CREATE TABLE brigade_availability (
    availability_id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    brigade_number          VARCHAR(20) NOT NULL,
    available_start         DATE NOT NULL,
    available_end           DATE NOT NULL,
    available_hours         INTEGER CHECK (available_hours IS NULL OR available_hours >= 0),
    current_assigned_hours  INTEGER NOT NULL DEFAULT 0 CHECK (current_assigned_hours >= 0),
    contact                 VARCHAR(50),
    notes                   TEXT,
    CONSTRAINT fk_brigade_availability_brigade
        FOREIGN KEY (brigade_number) REFERENCES brigades(brigade_number)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT chk_availability_dates CHECK (available_end >= available_start),
    CONSTRAINT chk_availability_hours CHECK (
        available_hours IS NULL OR current_assigned_hours <= available_hours
    )
);

CREATE INDEX idx_brigade_availability_period
    ON brigade_availability (brigade_number, available_start, available_end);

-- ============================================================================
-- 14. Месячный план ремонтов
-- ============================================================================
CREATE TABLE monthly_repair_plan (
    monthly_plan_id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_id               VARCHAR(20) NOT NULL UNIQUE,
    machine_id               VARCHAR(20) NOT NULL,
    model                    VARCHAR(100) NOT NULL,
    repair_type              VARCHAR(50) NOT NULL,
    required_specialization  VARCHAR(20) NOT NULL CHECK (required_specialization IN ('слесарь', 'электрик', 'сварщик', 'универсальная')),
    required_qualification   SMALLINT NOT NULL CHECK (required_qualification BETWEEN 1 AND 10),
    planned_start_date       DATE,
    planned_end_date         DATE,
    labor_hours              INTEGER NOT NULL CHECK (labor_hours >= 0),
    priority_weight          INTEGER NOT NULL CHECK (priority_weight >= 0),
    readiness_status         VARCHAR(20) NOT NULL CHECK (readiness_status IN ('готова', 'не готова')),
    notes                    TEXT,
    CONSTRAINT fk_monthly_repair_plan_request
        FOREIGN KEY (request_id) REFERENCES repair_requests(request_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT fk_monthly_repair_plan_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE INDEX idx_monthly_repair_plan_priority
    ON monthly_repair_plan (priority_weight DESC, planned_start_date, request_id);

-- ============================================================================
-- 15. Распределение бригад на ремонты
-- ============================================================================
CREATE TABLE repair_assignments (
    assignment_id       INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_id          VARCHAR(20) NOT NULL UNIQUE,
    machine_id          VARCHAR(20) NOT NULL,
    start_date          DATE,
    end_date            DATE,
    brigade_number      VARCHAR(20),
    planned_hours       INTEGER CHECK (planned_hours IS NULL OR planned_hours >= 0),
    responsible_person  VARCHAR(100),
    assignment_status   VARCHAR(20) NOT NULL CHECK (assignment_status IN ('назначена', 'не назначена')),
    notes               TEXT,
    CONSTRAINT fk_repair_assignments_request
        FOREIGN KEY (request_id) REFERENCES repair_requests(request_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT fk_repair_assignments_machine
        FOREIGN KEY (machine_id) REFERENCES machines(machine_id)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    CONSTRAINT fk_repair_assignments_brigade
        FOREIGN KEY (brigade_number) REFERENCES brigades(brigade_number)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

COMMIT;